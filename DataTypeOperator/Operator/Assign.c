#include<stdio.h>
int main()
{
	int x,y;
	x=18;
	y=21;
	printf("================= Assign Value ================\n");
	printf("	%d	>	%d	=	%d\n",x,y,x>y);
	printf("	%d	>=	%d	=	%d\n",x,y,x>=y);
	printf("	%d	<	%d	=	%d\n",x,y,x<y);
	printf("	%d	<=	%d	=	%d\n",x,y,x<=y);
	printf("	%d	==	%d	=	%d\n",x,y,x==y);
	printf("	%d	!=	%d	=	%d\n",x,y,x!=y);
	printf("*** Note: 1 = True  , 0 = false\n");
	printf("================= BAN SONITA ================\n");
	return 0;
}
